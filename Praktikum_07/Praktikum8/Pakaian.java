package Praktikum8;

public class Pakaian {
    String jenis, warna, merk, ukuran;
    double harga;
    //Objek pakaian
    Pakaian (String jenis, String warna, String merk, String ukuran, double harga){
        this.jenis = jenis;
        this.warna = warna;
        this.merk = merk;
        this.ukuran = ukuran;
        this.harga = harga;
        //this diguankan untuk merujuk pada objek saat ini dari suatu kelas
    }    
}



